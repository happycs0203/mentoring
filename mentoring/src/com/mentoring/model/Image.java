package com.mentoring.model;

public class Image {
	private int iNum;
	private int pNum;
	private String iTitle;
	private String iPath;
	private long iSize;
	
	public int getiNum() {
		return iNum;
	}
	public void setiNum(int iNum) {
		this.iNum = iNum;
	}
	public int getpNum() {
		return pNum;
	}
	public void setpNum(int pNum) {
		this.pNum = pNum;
	}
	public String getiTitle() {
		return iTitle;
	}
	public void setiTitle(String iTitle) {
		this.iTitle = iTitle;
	}
	public String getiPath() {
		return iPath;
	}
	public void setiPath(String iPath) {
		this.iPath = iPath;
	}
	public long getiSize() {
		return iSize;
	}
	public void setiSize(long iSize) {
		this.iSize = iSize;
	}
	
	
}
